package PracticalWork3;

public class Calc {
    static double sum(double a, double b) {
        return (a+b);
    }

    static double sub(double a, double b) {
        return (a-b);
    }

    static double mult(double a, double b) {
        return (a*b);
    }

    static double div(double a, double b) {
        return (a/b);
    }


}
